<h3 class="wp-block-heading has-h-4-font-size">1. Home Style</h3>



<p>First things first: what is the style of your home and neighborhood? Often the style of your home can help determine which siding color you should use.</p>



<p>The most popular home styles in <?php echo $row['country']; ?> include:</p>



<ul>
